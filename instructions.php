			<div class="option">
				<h1>Option 2: Panorama Mosaicker</h1>
				<ol>
					<li>Purchase the "Panorama Mosaicker" app ($1.99) for your iPhone or iPad 2 from the Apple app store.</li>
					<li>Open the app on your iPhone.</li>
					<li>Click "Import Photos."</li>
					<li>Stand up, tap the camera icon to take a panoramic video, and slowly spin in a circle.</li>
					<li>Stop the recording and run the "stitch" operation.</li>
					<li>Save the panorama.</li>
				</ol>
