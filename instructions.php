				<h1>Option 1: Hugin</h1>
				<ol>
					<li>Download and install <a href="http://hugin.sourceforge.net/" target="_blank">hugin</a>, a free, open source panoramic stitcher.</li>
					<li>Use a tripod, monopod, or flat surface to take pictures of the entire room by rotating the camera between shots.  Each one should overlap the next by about 25%.</li>
					<li>Transfer the pictures onto your computer.</li>
					<li>Load the pictures into hugin and run the stitching operation.</li>
					<li>Save the panorama.</li>
				</ol>
